package com.brandixi3.i3labs.nlp.model;

/**
 * The Class WordIndex.
 */
public class WordIndex {

	/** The begin. */
	private int begin;
	
	/** The end. */
	private int end;

	/**
	 * Gets the begin.
	 *
	 * @return the begin
	 */
	public int getBegin() {
		return begin;
	}

	/**
	 * Sets the begin.
	 *
	 * @param begin the new begin
	 */
	public void setBegin(int begin) {
		this.begin = begin;
	}

	/**
	 * Gets the end.
	 *
	 * @return the end
	 */
	public int getEnd() {
		return end;
	}

	/**
	 * Sets the end.
	 *
	 * @param end the new end
	 */
	public void setEnd(int end) {
		this.end = end;
	}
}
